// Crear variables

// Variables con var
var aprendiendo99 = true;

aprendiendo99 = false;
aprendiendo99 = 20;
aprendiendo99 = 'Nicolas';

// Se pueden crear variables sin asignarle valor 

var aprendiendo;

aprendiendo = 'JavaScript';

console.log(aprendiendo99);
console.log(aprendiendo);

// Variables con const

const aprendiendo22 = 'JavaScript';

// Al ser una constante sus valores no cambian 

console.log(aprendiendo22);

// Variables con let

let aprendiendo33 = 'JavaScript';

aprendiendo33 = true;

console.log(aprendiendo33);